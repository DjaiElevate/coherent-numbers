# ── §4 / §6 stable 404 on an ALLOWLISTED date: tolerated + coverage propagation

def _nov_window():
    """2022-11-06..2022-11-14, all present-in-both, one row per file (sqldate=f)."""
    present = [_d(2022, 11, d) for d in range(6, 15)]
    payloads = {f: _payload([_row(_iso(f))]) for f in present}
    file_map = {fp.expected_source_filename(d): payloads[d] for d in present}
    md5_b, size_b = _mk_manifests(file_map)
    return present, payloads, md5_b, size_b


class _Stable404Fetcher:
    """Serves payloads except for `absent_dates`, which always raise HTTP 404."""

    def __init__(self, payloads, absent_dates):
        self.payloads = payloads
        self.absent = set(absent_dates)
        self.dates = []

    def __call__(self, source_file_date, url):
        self.dates.append(source_file_date)
        if source_file_date in self.absent:
            raise _http_404(url)
        return self.payloads[source_file_date]


def test_stable_404_allowlisted_continues_as_tolerated_gap(monkeypatch, tmp_path):
    present, payloads, md5_b, size_b = _nov_window()
    miss = _d(2022, 11, 10)
    fetcher = _Stable404Fetcher(payloads, {miss})
    out = tmp_path / "out"
    res = fp.run_bounded_integrity_build(
        "2022-11-06", "2022-11-14", out_dir=str(out),
        md5sums_bytes=md5_b, filesizes_bytes=size_b, fetch_callable=fetcher,
        fetch_absent_allowlist=["2022-11-10"], sleep_fn=_FakeSleep(),
        **_guarded(monkeypatch))
    # Build COMPLETED (archive + manifest written), not aborted.
    assert (out / "ttg_approved_fields_archive.csv").exists()
    assert (out / "ttg_archive_provenance_manifest.json").exists()
    # The missing date is recorded as an allowlisted fetch-absent gap.
    assert res["fetch_absent_allowlisted_source_dates"] == ["2022-11-10"]
    assert "20221110.export.CSV.zip" in res["fetch_absent_allowlisted_source_files"]
    assert res["fetch_absent_allowlist_supplied"] == ["2022-11-10"]
    # It is NOT counted as a mutual-absence manifest gap.
    assert res["tolerated_gap_dates"] == []


def test_stable_404_allowlisted_coverage_propagation_two_sided(monkeypatch, tmp_path):
    present, payloads, md5_b, size_b = _nov_window()
    miss = _d(2022, 11, 10)
    fetcher = _Stable404Fetcher(payloads, {miss})
    res = fp.run_bounded_integrity_build(
        "2022-11-06", "2022-11-14", out_dir=str(tmp_path / "out"),
        md5sums_bytes=md5_b, filesizes_bytes=size_b, fetch_callable=fetcher,
        fetch_absent_allowlist=["2022-11-10"], sleep_fn=_FakeSleep(),
        **_guarded(monkeypatch))
    # Excluded: the missing source date AND both neighbors (support fan-out).
    for iso in ("2022-11-09", "2022-11-10", "2022-11-11"):
        assert iso not in res["primary_covered_sqldates"]
        assert iso in res["ledger_gap_uncovered_sqldates"]
        assert "20221110.export.CSV.zip" in res["coverage_ledger"][iso]["gap_causes"]
        assert iso in res["fetch_absent_coverage_loss_sqldates"]
    # Retained: neighbors one step further out (inside nominal primary window).
    for iso in ("2022-11-08", "2022-11-12"):
        assert iso in res["primary_covered_sqldates"]
        assert res["coverage_ledger"][iso]["state"] == fp.COVERAGE_COVERED
    # The exclusion routes through the SAME oracle the streamed row-filter uses.
    avail = set(present) - {miss}
    for iso in ("2022-11-09", "2022-11-10", "2022-11-11"):
        assert archive.fetched_set_fully_covers(datetime.date.fromisoformat(iso), avail) is False
    for iso in ("2022-11-08", "2022-11-12"):
        assert archive.fetched_set_fully_covers(datetime.date.fromisoformat(iso), avail) is True
    # The missing file was probed (initial + stability re-check) but never served.
    assert miss in fetcher.dates


