"""Missing-source retry/backoff + stable-404 + operator-authorized allowlist
amendment tests (no-network, value-blind) for the TTG archive build path.

Covers the amendment contract:
  1. Pre-amendment synthetic GOLDEN reproduction (non-circular): the golden was
     captured from clean 91c9f2b (fetch module SHA 1d069087…) BEFORE any edit;
     the amended code reproduces the exact archive + provenance-structural hashes
     under empty-allowlist all-success conditions. The test also proves the module
     CHANGED since capture, so the baseline was not generated by amended code.
  2. Transient fetch failure then success: bounded retry/backoff recovers; output
     equals the all-success baseline; streaming high-water stays 2*EDD+1 = 3.
  3. Retry exhaustion: transient beyond the bounded budget hard-fails cleanly.
  4. Stable 404 on an ALLOWLISTED date: deliberate tolerated fetch-absent gap;
     build continues.
  5. Stable 404 on a NON-allowlisted date: terminal hard-fail.
  6. Coverage propagation (two-sided): missing source 2022-11-10 (allowlisted)
     excludes SQLDATEs 2022-11-09/10/11 and retains 2022-11-08/12.
  7. Existing 2014 mutual-absence gap regression (unification did not perturb it).
  8. Empty-allowlist all-success equivalence (explicit empty allowlist param).
  9. Unknown stable 404 remains hard-fail (default empty/strict allowlist).
 plus: exception classifier unit checks; committed default allowlist is empty.

All tests run under an autouse no-network backstop with the source gate False, a
fake sleep_fn (no real-time sleep), and small bounded retry counts.
"""

