# ── §2 transient fetch failure then success (bounded retry recovers) ─────────

class _FlakyTransient:
    """Raises a transient exception the first `k` times a flaky date is requested,
    then serves its payload. Non-flaky dates serve immediately."""

    def __init__(self, payloads, flaky, exc_factory):
        self.payloads = payloads
        self.remaining = dict(flaky)
        self.exc_factory = exc_factory
        self.dates = []

    def __call__(self, source_file_date, url):
        self.dates.append(source_file_date)
        if self.remaining.get(source_file_date, 0) > 0:
            self.remaining[source_file_date] -= 1
            raise self.exc_factory()
        return self.payloads[source_file_date]


def _ten_day_window(base):
    present = [base + datetime.timedelta(days=i) for i in range(10)]
    payloads = {f: _payload([_row(_iso(f))]) for f in present}
    file_map = {fp.expected_source_filename(d): payloads[d] for d in present}
    md5_b, size_b = _mk_manifests(file_map)
    return present, payloads, md5_b, size_b


def test_transient_then_success_byte_identical_and_bounded_buffer(monkeypatch, tmp_path):
    base = _d(2015, 1, 1)
    present, payloads, md5_b, size_b = _ten_day_window(base)
    # Baseline: all-success run.
    base_res = fp.run_bounded_integrity_build(
        _iso(present[0]), _iso(present[-1]), out_dir=str(tmp_path / "base"),
        md5sums_bytes=md5_b, filesizes_bytes=size_b,
        fetch_callable=_Fetcher(payloads), sleep_fn=_FakeSleep(),
        **_guarded(monkeypatch))
    # Flaky: day 5 raises IncompleteRead twice (< budget 4), then succeeds.
    sleep = _FakeSleep()
    flaky = _FlakyTransient(payloads, {present[4]: 2}, lambda: http.client.IncompleteRead(b""))
    res = fp.run_bounded_integrity_build(
        _iso(present[0]), _iso(present[-1]), out_dir=str(tmp_path / "flaky"),
        md5sums_bytes=md5_b, filesizes_bytes=size_b,
        fetch_callable=flaky, sleep_fn=sleep, **_guarded(monkeypatch))
    # Byte-identical archive despite transient retries.
    assert res["archive_artifact"]["sha256"] == base_res["archive_artifact"]["sha256"]
    # Retry happened before buffering -> high-water stays the fixed bound 2*EDD+1.
    assert res["max_buffered_source_file_blocks"] == 2 * archive.ELIGIBILITY_DELTA_DAYS + 1
    assert res["max_buffered_source_file_blocks"] == 3
    assert base_res["max_buffered_source_file_blocks"] == 3
    # Backoff was actually exercised (>=2 transient retries slept).
    assert len(sleep.calls) >= 2


def test_transient_then_success_for_http_5xx(monkeypatch, tmp_path):
    base = _d(2015, 3, 1)
    present, payloads, md5_b, size_b = _ten_day_window(base)
