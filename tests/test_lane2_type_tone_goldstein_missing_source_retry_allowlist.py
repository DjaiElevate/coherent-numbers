# ── helpers (synthetic, value-blind) ─────────────────────────────────────────

_N = 58


def _row(sqldate_iso):
    cells = ["FILLER"] * _N
    cells[archive.DATE_FIELD_COLUMN_INDEX] = sqldate_iso.replace("-", "")
    cells[archive.QUADCLASS_COLUMN_INDEX] = "4"
    cells[archive.GOLDSTEINSCALE_COLUMN_INDEX] = "1.0"
    cells[archive.NUMMENTIONS_COLUMN_INDEX] = "5"
    cells[archive.AVGTONE_COLUMN_INDEX] = "0.5"
    return "\t".join(cells)


def _payload(rows):
    return ("\n".join(rows) + "\n").encode("utf-8")


def _d(y, m, day):
    return datetime.date(y, m, day)


def _iso(dt):
    return dt.isoformat()


def _mk_manifests(file_map):
    md5 = ["{}  {}".format(hashlib.md5(b).hexdigest(), fn) for fn, b in file_map.items()]
    siz = ["{} {}".format(len(b), fn) for fn, b in file_map.items()]
    return (("\n".join(md5) + "\n").encode(), ("\n".join(siz) + "\n").encode())


def _guarded(monkeypatch):
    monkeypatch.setattr(
        archive, "TYPE_TONE_GOLDSTEIN_LOCAL_ARCHIVE_BUILD_AUTHORIZED", True)
    return dict(cli_flag=True, module_authorized=True, env={archive.ENV_GUARD_NAME: "1"})


class _FakeSleep:
    """Records backoff durations; never sleeps in real time."""

    def __init__(self):
        self.calls = []

    def __call__(self, seconds):
        self.calls.append(seconds)


def _http_404(url="http://data.gdeltproject.org/x"):
    return urllib.error.HTTPError(url, 404, "Not Found", None, None)


def _http_503(url="http://data.gdeltproject.org/x"):
    return urllib.error.HTTPError(url, 503, "Service Unavailable", None, None)


