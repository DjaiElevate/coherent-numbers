# ── 4. t-1 windowing: the event day is never read ─────────────────────────────

def test_feature_window_ends_strictly_before_entry_date():
    spy = _spy_frame(400)
    entry = spy.dates[300]
    pos = spy.session_index_strictly_before(entry)
    assert spy.dates[pos] < entry
    assert pos == 299
    # a date equal to a session returns the prior session, not that session
    assert spy.session_index_strictly_before(spy.dates[0]) is None


