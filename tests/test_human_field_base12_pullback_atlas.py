# ── fixtures ──────────────────────────────────────────────────────────────────

def _spy_frame(n_sessions: int, start=date(2000, 1, 3), rvol_trend=False):
    """Ascending synthetic SPY frame. Calendar-day spacing is fine: the atlas
    indexes by *session position*, not calendar gaps."""
    dates = [start + datetime.timedelta(days=i) for i in range(n_sessions)]
    adj = np.array([100.0 * (1.0 + 0.0001 * i) for i in range(n_sessions)])
    if rvol_trend:
        # oscillation amplitude grows with i -> rolling std strictly
        # increasing in time (a linear ramp would have constant std).
        lr = np.array(
            [1e-4 * i * (1 if i % 2 == 0 else -1) for i in range(n_sessions)]
        )
    else:
        lr = np.array([0.001 if i % 2 == 0 else -0.001 for i in range(n_sessions)])
    return atlas.SpyFrame(dates=dates, adj_close=adj, log_return=lr)


def _reduced(entry_dates, directions, r_multiples, assets):
    n = len(entry_dates)
    is_long = np.array([d == "long" for d in directions], dtype=bool)
    return ReducedTrades(
        trade_id=np.arange(n, dtype=np.int64),
        asset=np.asarray(assets, dtype=object),
        entry_date=np.asarray(entry_dates, dtype=object),
        exit_date=np.asarray(entry_dates, dtype=object),
        is_long=is_long,
        r_multiple=np.asarray(r_multiples, dtype=np.float64),
        frozen_artifact_id=np.asarray(["synthetic"] * n, dtype=object),
    )


# ── 1. single-anchor phase function, not the 365-DOY surface ──────────────────

def test_uses_single_anchor_phase_function():
    # Binding check (robust): the single-anchor function is bound; the
    # 365-DOY surface function is NOT imported into the atlas namespace.
    assert atlas.assign_annual_sector_phase is (
        candidate_c_lens.assign_annual_sector_phase
    )
    assert not hasattr(atlas, "assign_anchor_shifted_phase")
    assert not hasattr(atlas, "enumerate_anchor_doys")


def test_phases_match_candidate_c_single_anchor(monkeypatch):
    def _boom(*a, **k):  # surface function must never be called
        raise AssertionError("assign_anchor_shifted_phase was called")

    monkeypatch.setattr(candidate_c_lens, "assign_anchor_shifted_phase", _boom)
    spy = _spy_frame(400)
    trades = _reduced([date(2001, 6, 1)], ["long"], [1.0], ["SPY"])
    ev = atlas.build_events(trades, spy)[0]
    assert ev.base12_phase == candidate_c_lens.assign_annual_sector_phase(
        date(2001, 6, 1), 12
    )
    assert ev.base10_phase == candidate_c_lens.assign_annual_sector_phase(
        date(2001, 6, 1), 10
    )
    assert 0 <= ev.base12_phase <= 11 and 0 <= ev.base10_phase <= 9


