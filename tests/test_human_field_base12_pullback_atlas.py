    rows = atlas.grid4_state_pss(events)
    populated = [r for r in rows if r["n"] > 0]
    assert populated
    for r in populated:  # all-long -> total variance 0 -> NA, not 0.0
        assert r["pss_12"] is atlas.NA
        assert r["pss_10"] is atlas.NA


