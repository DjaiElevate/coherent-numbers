# ── 7. indeterminate-state when lookback history is insufficient ──────────────

def test_indeterminate_on_insufficient_history():
    spy = _spy_frame(10)  # far fewer than 252 trailing sessions
    sf = atlas.compute_state_features(spy, spy.dates[-1] + datetime.timedelta(days=1))
    assert sf.state == atlas.STATE_INDETERMINATE
    assert sf.indeterminate_reason is not None


def test_state_resolves_with_enough_history():
    spy = _spy_frame(400, rvol_trend=True)
    entry = spy.dates[-1] + datetime.timedelta(days=1)
    sf = atlas.compute_state_features(spy, entry)
    assert sf.state in (
        atlas.STATE_CALM_BULL,
        atlas.STATE_MANIC,
        atlas.STATE_PANIC,
        atlas.STATE_QUIET_BEAR,
    )
    # rvol strictly increasing in time -> current is the max -> high arousal;
    # adj_close strictly increasing -> 30d return > 0 -> positive valence.
    assert sf.state == atlas.STATE_MANIC


# ── 8. low-data flag n < 20 ───────────────────────────────────────────────────

def test_low_data_flag_threshold():
    spy = _spy_frame(400, rvol_trend=True)
    eds = [date(2001, 6, 1)] * 5  # only 5 events -> every populated cell low-data
    trades = _reduced(eds, ["long"] * 5, [1.0] * 5, ["SPY"] * 5)
    events = atlas.build_events(trades, spy)
    cells = atlas.grid1_event_count(events)
    populated = [c for c in cells if c.n > 0]
    assert populated and all(c.low_data and c.marker == "LOW_DATA" for c in populated)
    # full cartesian still emitted (incl. empty cells)
    assert len(cells) == atlas.BASE12 * len(atlas.STATE_ORDER)


# ── 9. degenerate PSS -> NA, never silently 0.0 ───────────────────────────────

def test_degenerate_pss_returns_na():
    spy = _spy_frame(400, rvol_trend=True)
    eds = [date(2001, 6, 1), date(2001, 9, 1)]
    trades = _reduced(eds, ["long", "long"], [1.0, 2.0], ["SPY", "SPY"])
