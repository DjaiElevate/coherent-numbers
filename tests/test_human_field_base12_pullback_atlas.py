# ── preflight QA: indeterminate count > 0 blocks the summary ──────────────────

def test_preflight_flags_indeterminate(monkeypatch):
    spy = _spy_frame(400, rvol_trend=True)
    eds = [date(2001, 6, 1)] * atlas.EXPECTED_POOLED_ROW_COUNT
    assets = (
        ["SPY"] * 243 + ["EFA"] * 283 + ["EEM"] * 261
        + ["GLD"] * 253 + ["TLT"] * 242
    )
    trades = _reduced(eds, ["long"] * len(eds), [1.0] * len(eds), assets)
    events = atlas.build_events(trades, spy)
    # force one indeterminate event
    events[0] = atlas.AtlasEvent(
        **{**events[0].__dict__, "state": atlas.STATE_INDETERMINATE,
           "indeterminate_reason": "synthetic"}
    )
    status, details = atlas.preflight(trades, spy, events)
    assert status == "implementation_check_failed"
    assert details["indeterminate_count"] == 1
    assert "qa_warning" in details


def test_canonical_runner_refuses_without_authorization():
    import importlib.util
    import os

    path = os.path.join(
        os.path.dirname(__file__), "..", "scripts",
        "run_human_field_base12_pullback_atlas.py",
    )
    spec = importlib.util.spec_from_file_location("hf_runner", path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    assert mod.CANONICAL_RUN_AUTHORIZED is False
    with pytest.raises(SystemExit):
        mod.run_canonical(os.getcwd())
